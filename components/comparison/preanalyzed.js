export const serialize = (analysis) => [
  analysis.name,
  analysis.algorithm,
  analysis.dataSetName,
  analysis.dataSetSize,
  analysis.actualOperations,
  analysis.expectedOperationsBest,
  analysis.expectedOperationsAverage,
  analysis.expectedOperationsWorst,
];

export const deserialize = (array) => ({
  name: array[0],
  algorithm: array[1],
  dataSetName: array[2],
  dataSetSize: array[3],
  actualOperations: array[4],
  expectedOperationsBest: array[5],
  expectedOperationsAverage: array[6],
  expectedOperationsWorst: array[7],
});

export function persist(name, analyses) {
  const serialized = analyses.map(serialize);
  const data = JSON.parse(localStorage.getItem("preanalyzed") || "{}");
  data[name] = serialized;
  const json = JSON.stringify(data);
  localStorage.setItem("preanalyzed", json);
}

